ASN1_TIME *
ossl_x509_time_adjust(ASN1_TIME *s, VALUE time)
{
time_t sec;
int off_days;
ossl_time_split(time, &sec, &off_days);
return X509_time_adj_ex(s, off_days, 0, &sec);
}
